    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        // TODO add your handling code here:
        int resto = Integer.parseInt(txtResto.getText());
        int minuendo = Integer.parseInt(lblMinuendo.getText());
        int subtraendo = Integer.parseInt(lblSubtraendo.getText());
        int diferenca = minuendo - subtraendo;
        if(resto==diferenca){
            lblResposta.setText("RIGHT!!");
        }else{
            lblResposta.setText("WRONG!!");
        }
        lblResto.setText(Integer.toString(diferenca));
        panResultado.setVisible(true);
    }//GEN-LAST:event_btnVerificarActionPerformed

